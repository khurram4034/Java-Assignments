var n1 = +prompt("Please Enter a Number ");
var o = prompt("Please Enter Operation ");
var n2 = +prompt("Please Enter a Number ");


if(o == "+"){
    alert("Your Result is : " + (n1+n2));
}
else if(o == "-"){
    alert("Your Result is : " + (n1-n2));
}
else if(o == "*"){
    alert("Your Result is : " + (n1*n2));
}
else if(o == "/"){
    alert("Your Result is : " + (n1/n2));
}
else if(o == "%"){
    alert("Your Result is : " + (n1%n2));
}
else{
    alert("Please Enter the Correct Operator");
}






